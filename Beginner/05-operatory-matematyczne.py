# Przypisanie wartosci zmiennym
a = 5
b = 10

# Wypisanie wynikow podstawowych operacji matematycznych
print(a + b)
print(a - b)
print(a * b)
print(a / b)

# Wypisanie typow wynikow podstawowych operacji matematycznych
print(type(a + b))
print(type(a - b))
print(type(a * b))
print(type(a / b))

# Dzielenie z reszta
print(a % b)

# Podnoszenie do potegi
print(a ** b)

# Dzielenie calkowite
print(a // b)
